package org.betavzw.hfdstk3.data;

public class Cursus {

	private String code;

	private String naam;

	private int duur;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getNaam() {
		return naam;
	}

	public void setNaam(String naam) {
		this.naam = naam;
	}

	public int getDuur() {
		return duur;
	}

	public void setDuur(int duur) {
		this.duur = duur;
	}

}
